$(document).ready(function() {
  const checkoutButton = $(".payButton");

  checkoutButton.on("click", function() {
    // Collect payment data
    const name = $(".payInput[name='name']").val();
    const phone = $(".payInput[name='phone']").val();
    const address = $(".payInput[name='address']").val();
    const cardNumber = $(".payInput[name='cardNumber']").val();
    const expiryMonth = $(".payInput[name='expiryMonth']").val();
    const expiryYear = $(".payInput[name='expiryYear']").val();
    const cvv = $(".payInput[name='cvv']").val();

    // Send payment data to process_payment.php using AJAX
    $.ajax({
      type: "POST",
      url: "process_payment.php",
      data: {
        name: name,
        phone: phone,
        address: address,
        cardNumber: cardNumber,
        expiryMonth: expiryMonth,
        expiryYear: expiryYear,
        cvv: cvv
      },
      success: function(response) {
        // Handle the response from the server if needed
        console.log(response);

        // Close the payment form
        $(".payment").hide();
      },
      error: function(error) {
        // Handle errors if any
        console.log(error);
      }
    });
  });
});
